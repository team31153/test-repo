#!/usr/bin/env python3
x = input("WHat is the day")
x = int(x)
def day(x):
    if x == 1:
        print("Sunday")
    if x == 2:
        print("Monday")
    if x == 3:
        print("Tuesday")
    if x == 4:
        print("Wednesday")
    if x == 5:
        print("Thursday")
    if x == 6:
        print("Friday")
    if x == 7:
        print("Saturday")
day(x)
